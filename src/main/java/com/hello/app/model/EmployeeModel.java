package com.hello.app.model;

public class EmployeeModel {

	public Integer employeeUid;
	public String employeeFirstName;
	public String employeeLastName;
	public String employeeMobileNum;
	
	public Integer getEmployeeUid() {
		return employeeUid;
	}
	public void setEmployeeUid(Integer employeeUid) {
		this.employeeUid = employeeUid;
	}
	public String getEmployeeFirstName() {
		return employeeFirstName;
	}
	public void setEmployeeFirstName(String employeeFirstName) {
		this.employeeFirstName = employeeFirstName;
	}
	public String getEmployeeLastName() {
		return employeeLastName;
	}
	public void setEmployeeLastName(String employeeLastName) {
		this.employeeLastName = employeeLastName;
	}
	public String getEmployeeMobileNum() {
		return employeeMobileNum;
	}
	public void setEmployeeMobileNum(String employeeMobileNum) {
		this.employeeMobileNum = employeeMobileNum;
	}
	
	
}
